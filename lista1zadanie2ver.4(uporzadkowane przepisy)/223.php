	<summary>Opinion</summary>
<p>Thanksgiving is rapidly approaching and everybody is searching for the best ideas for this special dinner. For all those who consider a whole turkey might be too much this roasted turkey breast might be a wonderful solution. It is really easy to prepare, takes no more than 10 minutes to make the marinade, and once roasted you will have a wonderful looking roasted turkey, moist and flavorful.</p>
<p>Love this marinade, as it allows the roasting of the turkey breast without the skin, and it still remains moist and delicious. Not to mention that is healthier this way as well. The use of mustard is definitely a winner for me and will use it in many marinades for either chicken or turkey.</p>
<p>This roasted turkey is suitable not only for holiday tables but for any other day as it is quick, and can be used in various recipes for the next days either for sandwiches, or use it for enchiladas, salads and so on.</p>
