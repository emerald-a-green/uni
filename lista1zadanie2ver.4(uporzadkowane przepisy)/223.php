<h3>Ingredients:</h3>
Makes 6 to 8 servings
<ul style="list-style-type:circle">
<li>3 pounds turkey breast, boneless, skinless </li>
<b><i>Marinade</b></i>
<li>3 tbsp Dijon mustard </li>
<li>2 tbsp balsamic vinegar </li>
<li>3 tsp dried herbs (thyme, oregano, rosemary, bay leaves, your choice) </li>
<li>2 tsp garlic powder </li>
<li>1 tsp salt </li>
<li>3 tbsp olive oil </li>
<li>1 tsp smoked paprika </li>
<li>Freshly ground black pepper </li>
<b><i>Additional Ingredients for Drippings gravy, optional</b></i>
<li>1 tbsp flour </li>
<li>1 cup vegetable stock </li>
</ul>
	</section>
	<section>
		<h3>Directions</h3>
		<ol type="1"> 
<li>Preheat the oven to 350F (180C). Rinse the turkey well and pat it dry.</li> 
<li>Place all the marinade ingredients, mustard, vinegar, herbs, salt and pepper, garlic, smoked paprika and olive oil,  into a bowl and stir to combine.</li>
<li>Rub the mixture all over the turkey. Refrigerate to marinate overnight, if possible, or leave at least 1 hr before roasting.</li>
<li>Place the turkey breast into a baking pan, or baking sheet.</li>
<li>Roast for 45 minutes to one hour, until the thermometer reaches 170F (77C). </li>
<li>Let it stand for a couple of minutes and slice to serve. </li>
<li>Prepare the gravy if desired. After removing the turkey to a serving platter, place the pan to the stove on medium heat. Add the flour. Stir to combine. Add the stock, stir and let it cook until thickened and smooth.</li>
	</ol>
