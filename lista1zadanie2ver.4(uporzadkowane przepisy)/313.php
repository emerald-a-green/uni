<p>This is the first time I have ever paired pears with chocolate and I must say that I really loved it and I didn’t expect to be such a pleasant combination. If you like pears and chocolate you should definitely try this dessert. It is very flavorful due to the pears and almonds, and also very moist and rich which makes it simply irresistible.</p>
