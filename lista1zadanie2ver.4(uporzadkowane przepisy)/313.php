<h3>Ingredients:</h3>
Makes about 10 servings
<ul style="list-style-type:circle">
<li>4 eggs, separated</li>
<li>7 oz (200g) semisweet chocolate (55-70%)</li>
<li>2/3 cup (150g) butter</li>
<li>1 tbsp (8g) unsweetened cocoa powder</li>
<li>1 cup (100g) ground almonds</li>
<li>1/4 tsp (2g) salt</li>
<li>1 tsp (5ml) almond extract</li>
<li>1/2 cup (100g) sugar</li>
<li>3 or 4 ripe pears, peeled, halved and cored</li>
</ul>
	</section>
	<section>
		<h3>Directions</h3>
		<ol type="1"> 
<li>Grease and line a 10-inch (25 cm) springform pan (with a removable base) with parchment paper. Separate egg yolks from whites. Preheat oven to 180°C (350°F). </li>
<li>Melt chocolate and butter over a bain-marie until smooth. Let it cool slightly. Add yolks one at a time and stir well. Stir in cocoa powder, ground almonds, salt and almond extract. </li>
<li>Whip egg whites until foamy using a mixer. Gradually add sugar and whip until soft peaks form.  Gently fold the whipped whites into chocolate mixture. </li>
<li>Pour batter into prepared pan. </li>
<li>Cut pear halves in thin slices leaving one edge uncut. </li>
<li>Place the sliced pear halves on top of the batter and bake for 35-40  minutes. </li>
<li>Let it cool completely and refrigerate to set for at least 2 hours.</li>

	</ol>
