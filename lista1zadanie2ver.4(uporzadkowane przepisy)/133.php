	<summary>Opinion</summary>
<p>Chocolate Meringue Roses are a delicate and elegant dessert that can be made in advance for any occasion and everybody will be impressed and delighted. The meringues are dipped in dark chocolate which is balancing very well their sweetness. With their crispy texture these meringues completely melt in your mouth.</p>
<p>Making meringues is not difficult at all. You just need to whip the whites to stiff peaks and give any shape you like. In order for the whites to whip well is to make sure no yolks or schell gets in the whites or they will fail to whip. Let them dry completely to get the crispy texture and enjoy.</p>
