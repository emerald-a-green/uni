<h3>Ingredients:</h3>
Makes 23-25 meringues
<ul style="list-style-type:circle">
<li>2 egg whites (70g), room temperature</li>
<li>pinch of salt</li>
<li>1/2 cup (100g) sugar</li>
<li>1/2 tsp (3g) vanilla extract</li>
<li>Red food coloring</li>
<li>4 oz (120g) dark chocolate</li>
</ul>
	</section>
	<section>
		<h3>Directions</h3>
		<ol type="1"> 
<li>Preheat oven to  200F (95C). </li>
<li>Whip egg whites with salt until foamy. Gradually add sugar and whip until firm peaks form. Add vanilla extract and red food coloring and mix to combine. </li>
<li>Fit a piping bag with 1M (½ inch- 1cm) large star tip. Make 4 lines of red food coloring on the inside of the piping bag. </li>
<li>Transfer the meringue into the piping bag. </li>
<li>On a baking sheet lined with parchment paper make 2 inch (5cm) roses starting from the center and making circles around. </li>
<li>Bake for 90 minutes. Turn off the oven and leave the meringues in the oven until the oven is completely cooled, about 2-3 hours more or even overnight. </li>
<li>Melt chocolate over bain-marie. </li>
<li>Dip bottoms in chocolate and place on parchment paper to set. </li>
<li>Store in an airtight container at room temperature.</li>

	</ol>
