	<summary>Opinion</summary>
<p>I simply love making choux pastry either for eclairs or cream puffs (profiteroles). As they bake they create a hole inside which allows either sweet or savory fillings.  This time I wanted to make chocolate cream puffs, filled with chocolate pastry cream and topped with chocolate ganache. They turned out really delicious and a perfect treat for chocolate lovers.</p>
<p>Though the recipe is easy it does take a bit of time due to cooling process but in the end all the waiting is rewarded.  If you are in a hurry you can simply use some whipped cream or ice cream for the filling and in this way you can save some time. They are best served in the same day after filling to be able to enjoy the delicate crispy texture of choux pastry in combination with the creamy pastry cream.</p>
