<h3>Ingredients:</h3>
Makes about 25 cream puffs
<ul style="list-style-type:circle">
<b><i>Choux Pastry</b></i>
<li>1 cup (125g) all-purpose flour</li>
<li>3.5 oz (100ml) milk</li>
<li>3.5 0z (100ml) water</li>
<li>2 tsp (10g) sugar</li>
<li>1/2 tsp (2g) salt</li>
<li>5 1/2 tbsp (80g) unsalted butter</li>
<li>4 eggs</li>
<b><i>Chocolate Pastry Cream</b></i>
<li>4 egg yolks</li>
<li>1/3 cup (70g) sugar</li>
<li>1/3 cup (40g) cornstarch</li>
<li>2 cups (480 ml) milk</li>
<li>4 oz (120g) semisweet chocolate, chopped</li>
<li>1 tbsp (15g) unsalted butter</li>
<b><i>Chocolate Ganache</b></i>
<li>4 oz (120g) semisweet chocolate</li>
<li>1/2 cup (120g) whipping cream</li>
</ul>
	</section>
	<section>
		<h3>Directions</h3>
		<ol type="1"> 
<li>First prepare the pastry cream to have it ready by the time the choux pastries are baked. </li>
<li>Whisk the egg yolks with sugar until slightly pale. </li>
<li>Incorporate the cornstarch. Pour the milk into a saucepan and bring to a simmer. Pour about a third of the hot milk over the egg yolks mixture. Pour the whole egg yolks mixture into the saucepan over the milk. Bring to a boil while stirring and thickens. Keep enough over heat to cook all the cornstarch. Remove from heat, add chocolate and keep stirring until chocolate is melted completely. </li>
<li>Pour the cream into a clean bowl and allow to cool for 5 minutes,  then incorporate the butter while stirring. Cool slightly, cover with plastic wrap, pressing it directly onto the surface of the pastry cream to prevent forming a skin on the surface. Refrigerate until ready to use. </li>
<li>Preheat oven to 350F (180C). </li>
<li>Prepare the choux pastry. Sift the flour. In a saucepan bring the milk, water, sugar, salt and butter to a boil. </li>
<li>Remove from heat and add flour all at once and incorporate mixing energetically with a wooden spoon until homogenous. </li>
<li>Return the saucepan over low heat and while stirring cook for 1 minute or two to pull out the moisture from the batter and until it pulls away from the sides of the pan. You will see some of the dough sticks to the bottom of the pan. </li>
<li>Transfer batter to a large bowl and allow to cool slightly. Add the eggs one at a time, carefully incorporating each into the batter using the wooden spoon or even a stand mixer. It will result in a smooth, homogenous batter which still holds its shape. </li>
<li>Fit a pastry bag with a large ½ inch (1cm) round tip and pipe the dough into 1- 1 1/2 inch (3-4 cm) circles on a parchment paper lined baking sheet. </li>
<li>Bake  for 25-30 minutes until browned and puffed. </li>
<li>Prick each with a skewer to release steam and allow to cool on a wire rack. Split the cream puffs in half. </li>
<li>Remove pastry cream from refrigerator and whisk until smooth. Transfer the pastry cream into a piping bag fitted with 1/4 inch (6 mm) plain or star tip. </li>
<li>Pipe pastry cream to fill the cream puffs. Top with the pastry caps. </li> 
<li>Prepare the chocolate ganache. In a small saucepan, place cream over medium-low heat for a few minutes to get hot. No boiling or simmering needed. Remove from heat, pour over chopped chocolate and let rest for 1-2 minutes. Stir until dissolved. </li>
<li>Use a spoon or transfer the ganache into a piping bag fitted with 1/8 inch (3mm) plain tip and drizzle the ganache over the top of cream puffs. </li>
<li>Serve in the same day after filling. </li>

	</ol>
