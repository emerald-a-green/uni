<h3>Ingredients:</h3>
Make about 6 servings
<ul style="list-style-type:circle">
<li>2 pounds (900g) chicken breast</li>
<li>2 cups (120g) panko bread crumbs</li>
<li>1/2 cup (50g) Parmesan, grated</li>
<li> tsp (2g) smoked paprika</li>
<li>1 tsp (3g) garlic powder</li>
<li>1 tbsp (3g) dried parsley (or any other herbs)</li>
<li>1 tbsp vegetable oil</li>
<li>1 cup (125g) all purpose flour</li>
<li>2 2 eggs</li>
<li>Salt and freshly ground black pepper</li>
</ul>
	</section>
	<section>
		<h3>Directions</h3>
		<ol type="1"> 
<li>Preheat oven to 400F (200C). Line 1 or 2 baking sheets with parchment paper. </li>
<li>In a large bowl combine panko breadcrumbs with Parmesan cheese, paprika, garlic powder, dried parsley, salt and pepper. Set aside until ready to use. </li>
<li>Cut chicken breast in about 1 ½ inch (4cm) bite size pieces. Season chicken with salt and pepper. Into a large bowl add flour and chicken pieces. Use a spoon to toss the chicken into the flour until all pieces are coated. Transfer the chicken to another clean bowl and discard the excess flour. </li>
<li>Beat eggs with oil and pour over the chicken and toss to coat. </li>
<li>Roll each piece of chicken in the panko Parmesan mixture. Transfer the pieces to the prepared pans. </li>
<li>Bake for about 25 minutes until golden brown. </li>
<li>Serve with your favorite dipping sauce.</li>
	</ol>
