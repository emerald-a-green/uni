	<summary>Opinion</summary>
<p>Not too long ago I’ve seen an image of a triple chocolate French parfait and really got my attention. Each chocolate had a different layer and that was challenging enough for me to want to give a try. I’ve used a different recipe though,  an easier semifreddo one I was already used with, and changed it accordingly. I have reduced the amount of sugar used as chocolates bring enough sweetness as well.</p>
<p>I am really pleased of how this recipe turned out. This triple chocolate semifreddo looks incredible, quite elegant and I think it’s not necessary to say how delicious this is. It’s creamy and chocolaty, not too sweet, with bites of crunchy chocolate curls,  by far one of the best treats for summer days.</p>
<p>The recipe is easy and worth all the time involved.  It is a great dessert you can impress your family or friends with and you can literally say you are in chocolate heaven, no doubt about that.</p>
