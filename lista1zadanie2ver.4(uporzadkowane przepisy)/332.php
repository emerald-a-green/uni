<h3>Ingredients:</h3>
Makes about 8-10 servings
<ul style="list-style-type:circle">
<li>3 eggs</li>
<li>2 egg yolks, extra</li>
<li>1/2 cup (100g) sugar</li>
<li>2 cups (500ml) whipping cream (35% fat)</li>
<li>3.5 oz (100g) white chocolate</li>
<li>3.5 oz (100g) milk chocolate</li>
<li>3.5 oz (100g) dark chocolate</li>
<li>chocolate curls, for decorating</li>
</ul>
	</section>
	<section>
		<h3>Directions</h3>
		<ol type="1"> 
<li>Place each chocolate in a heatproof bowl over a pan of simmering water (bain-marie). Allow the chocolates to melt, stirring occasionally. Set aside until ready to use. </li>
<li>Place the eggs, extra yolks and sugar in a heatproof bowl. Place over a saucepan of simmering water and, using a hand-held electric mixer, beat for 6–8 minutes or until thick and pale. Remove from the heat and allow to cool slightly. </li>
<li>Whisk the cream until stiff peaks form. Gently fold through the egg mixture until well combined. </li>
<li>Divide the mixture into three equal-sized amounts,  and fold the chocolates, each into a different bowl. </li>
<li>Line a 2 litre-capacity  loaf tin with plastic wrap and pour in the white chocolate mixture.  Freeze for 20 minutes or until partially set, leaving the other mixtures at room temperature. </li>
<li>Gently pour in the milk chocolate mixture over the white and spread evenly. Freeze for another 20 minutes or until partially set. </li>
<li>Gently pour in the dark chocolate mixture on top of the milk one. Freeze for 4-6 hours or overnight. </li>
<li>When ready to serve invert onto a plate, remove the plastic wrap, decorate if desired with chocolate curls or grated chocolate and cut into slices.</li>
	</ol>
