	<summary>Opinion</summary>
<p>Here is another quick and easy potato recipe that you can either prepare as a side dish or simply as a snack.  For these pancakes the potatoes are not cooked in advance but grated and squeezed well of any excess liquid. This step is really important to ensure getting delicious crispy pancakes on the outside.</p>
<p>Potatoes have always made a great pair with cheese and onion so these ingredients are a must as well. As for the spices besides garlic you can add any others you like best, like paprika, chili, herbs and so on.</p>
<p>These Cheese Potato Pancakes turned really great. Really crispy on the outside while the interior is soft and deliciously flavored. You should probably double the quantities as they go really fast. Everybody enjoyed them even the kids. They also can be a great idea for their lunch boxes or to take on the go. Enjoy!</p>
