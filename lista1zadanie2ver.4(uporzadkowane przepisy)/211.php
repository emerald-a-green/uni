<h3>Ingredients:</h3>
Makes about 15 pancakes
<ul style="list-style-type:circle">
<li>2 pounds (900g) potatoes, peeled and grated
<li>1/2 cup (50g) Cheddar cheese
<li>1/2 cup (50g) Parmesan cheese
<li>3 green onions, chopped
<li>1/2 tsp (2g) garlic powder
<li>1/4 cup (30g) all-purpose flour
<li>2 eggs, beaten
<li>1 tsp (5g) salt
<li>Freshly ground black pepper
<li>1/4 cup (50g) oil, for frying</li>
</b></i>Dipping sauce</b></i>
<li>1/2 cup (<li><li>120g) sour cream
<li>1 tbsp (20g) chili garlic sauce</li>
</ul>
	</section>
	<section>
		<h3>Directions</h3>
		<ol type="1">
<li>Use the large holes of the grater to grate cheese and potatoes. 
<li>Transfer the potatoes into a kitchen towel and squeeze as much liquid as you possible can. 
<li>Place the potatoes into a large bowl. Add cheeses, onion, garlic powder, flour, salt, pepper and eggs. Stir to combine. 
<li>Heat oil over medium high heat. Use an ice cream scoop to take equal amounts of potato mixture and add to the pan. Use the back of a spoon to flatten the patties. 
<li>Cook about 3 or 4 patties at a time. 
<li>Cook until browned about 4 minutes per side. 
<li>Repeat with remaining potato mixture. 
<li>Transfer the pancakes to a plate lined with paper towel to remove excess oil. 
<li>Prepare the dipping sauce by mixing sour cream with  chili garlic sauce. 
<li>Serve the potato pancakes while still warm.</li>
	</ol>
