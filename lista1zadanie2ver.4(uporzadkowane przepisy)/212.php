	<summary>Opinion</summary>
<p>Why make fried chicken when the baked one looks and taste soo incredible good.  Definitely a healthier alternative,  the chicken remain moist inside and really crispy and crunchy on the outside due to the cornflakes crust. You can add various spices to the cornflakes mixture like garlic or chili powder or various herbs. It's so easy to prepare it that it would be a shame not to do these baked cornflake crusted chicken strips at home for whenever you are craving for some. No more guilt involved :)</p>
