<h3>Ingredients:</h3>
Makes about 4 servings (12-14 strips)
<ul style="list-style-type:circle">
<li>2 boneless, skinless chicken breast halves</li>
<li>1 large egg or 2 smaller ones</li>
<li>3 cups cornflakes</li>
<li>1/2 tsp salt</li>
<li>1 tbsp fresh parsley, chopped</li>
<li>1 tbsp olive oil</li>
<li>4 tbsp flour</li>
<li>Freshly ground black pepper</li>
</ul>
	</section>
	<section>
		<h3>Directions</h3>
		<ol type="1"> 
<li>Preheat oven to 400F (200C) and line a baking sheet with parchment paper. Rinse chicken under cold water and pat dry. Cut the chicken in long strips about 1/2 inch thick. Season with salt and pepper.</li>
<li>Beat the eggs with oil and set aside. </li>
<li>Place the flour on a plate and set aside. Place the cornflakes into a Ziploc bag, seal it and using o rolling pin or simply your hands crush the cornflakes into small pieces.</li>
<li>Put the crushed cornflakes into a flat bowl and stir in salt, pepper and parsley. </li>
<li>Taking one piece at a time coat chicken strips in flour, then dip in egg mixture and finally coat with seasoned cornflakes, pressing flakes with a fork to help them stick better.Transfer each piece into the prepared baking sheet. Discard remaining flour, egg and cornflakes.</li>
<li>Bake for about 30 minutes until golden brown and crisp, flipping them on the other side after the first 15 minutes. </li>
<li>Serve with your favorite dipping.</li>
	</ol>
