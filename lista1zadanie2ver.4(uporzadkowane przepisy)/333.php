<h3>Ingredients:</h3>
Makes about 12 servings
<ul style="list-style-type:circle">
<b><i>Chocolate Cake Batter</b></i>
<li>7 eggs, separated
<li>2/3 cup (150g) butter, room temperature
<li>1 cup (120g) powdered sugar
<li>5 oz (150g) bittersweet chocolate, chopped
<li>1 tsp (5g) vanilla extract
<li>1/2 cup +1 tbsp (120g) sugar
<li>A pinch of salt
<li>1 1/4 cups (160g) all-purpose flour</li>
<b><i>Apricot Jam (250-300g jam)</b></i>
<li>1 pound (450g) fresh apricots, seeds removed
<li>2/3 cup (135g) sugar
<li>1 tbsp (15ml) lemon juice
<li>1 tbsp (15ml) water</li>
<b><i>Chocolate Glaze</b></i>
<li>7 oz bittersweet chocolate (85% cocoa)
<li>1 1/4 cups (250g) sugar
<li>2/3 cup (160ml) water</li>
<b><i>Garnish</b></i>
<li>Unsweetened whipped cream</li>
</ul>
	</section>
	<section>
		<h3>Directions</h3>
		<ol type="1">
<li>Preheat oven to 350F (180C). Grease with butter and dust with flour a 9 inch (23 cm) round cake pan. 
<li><b><i>Prepare the cake batter.</b></i> In a large bowl mix the butter with powdered sugar until creamy. Add egg yolks one at a time and stir in vanilla extract. 
<li>Meanwhile melt the chocolate in a double boiler and add it to the yolks mixture. 
<li>Whip egg whites with salt until foamy. Gradually add sugar and continue whipping until soft peaks form. 
<li>Gently fold the whites into the chocolate mixture. Gradually incorporate sifted flour into the mixture. 
<li>Pour batter into the prepared pan and bake for about 45-50 minutes until a toothpick inserted into the center of the cake comes out clean. 
<li>Invert the cake to get a flat surface and cool completely. 
<li>Meanwhile <b><i>prepare the apricot jam.</b></i>  Cut apricots in smaller pieces and place into a saucepan with sugar, water and lemon juice. Bring to a boil while stirring constantly. Simmer for about 20-30 minutes until thickens and remove from heat. Sieve to get a smooth jam. Set aside until ready to use. <li><b><i>Assembling the cake.</b></i> Divide cake in two layers. Spread about half of the hot jam over the first layer. Top with the second cake layer and spread the rest of hot jam on top and sides of the cake. Let it dry for up to 30 minutes. 
<li>Transfer the cake over a wire rack placed on a parchment paper lined baking sheet. 
<li><b><i>Prepare the chocolate glaze.</b></i> Add sugar and water into a saucepan. Bring to boil for about 5 minutes or until thermometer reaches 234?F/112°C. Remove from heat, stir for several times and add chopped chocolate. Stir until smooth and shiny.  If it thickens too much return it over low heat and add a few drops of water if necessary to thin it out to a runny, pourable consistency. 
<li>Pour the hot glaze all over the cake, starting from the edges, center. You can use the leftover glaze from the baking sheet to decorate the cake later or refrigerate for later use. Wait for few minutes and transfer the cake to a serving platter. Let the glaze set for at least one hour at room temperature. Don’t refrigerate as it might affect the glaze. 
<li>Use leftover chocolate glaze for writing Sacher on top of the cake. 
<li>Serve alongside unsweetened whipped cream.
	</ol>
