	<summary>Opinion</summary>
	<p>Sacher Torte is one of the most famous cakes around the world. In 1832  Franz Sacher created the Sacher Torte at the court of Prince Metternich, in Vienna, Austria. It is an elegant, soft chocolate cake, filled with apricot jam and topped with a delicious chocolate glaze.  It is best served alongside unsweetened whipped cream and a good coffee. The recipe of the Original Sacher Torte is still a well-kept secret known only by the pastry chefs of Hotel Sacher in Vienna but similar recipes appeared ever since. </p>
<p>As apricot season is here I was really eager to try this recipe I’ve been keep hearing about. I love chocolate cakes and the idea of combining freshly made apricot jam with a chocolate cake seemed absolutely amazing.</p>
<p>The recipe of the cake is very simple, the hardest part may be the chocolate glaze. It is important to obtain the right consistency and use good quality chocolate.</p>
<p>The cake doesn’t need to be refrigerated and sets at room temperature. The texture and flavor is absolutely amazing, sweet but not overwhelmingly sweet, very well balanced if served with whipped cream and made with bittersweet chocolate.</p>
