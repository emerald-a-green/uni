	<summary>Opinion</summary>
<p>Rich, fudgy, chocolaty brownies topped with swirled cream cheese are simply hard to resist.</p>
<p>I make them for many years now on special occasions and are greatly appreciated each time. Even though I already posted a similar recipe on my blog years ago I’ve wanted to make few changes and create the perfect swirled brownies ever. This time the swirls are more visible and clear and the brownies are more fudgy and dense. This time the butter is melted with chocolate and the batter is more runny and makes it easier to create swirls on top.</p>
<p>This recipe uses orange, almond, espresso and vanilla for extra flavor, but feel free to change these according to your tastes.</p>
