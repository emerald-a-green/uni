<h3>Ingredients:</h3>
Makes about 15 servings
<ul style="list-style-type:circle">
<b><i>Brownie Batter</b></i>
<li>2/3 cup (150g) butter</li>
<li>7 oz (200g) semisweet chocolate</li>
<li>2/3 cup (135g) sugar</li>
<li>4 eggs</li>
<li>1/4 cup (50ml) espresso</li>
<li>1 tsp (5g) vanilla extract</li>
<li>1/2 tsp (2g) almond extract</li>
<li>3 tbsp (24g) unsweetened cocoa powder</li>
<li>1 cup (125g) flour</li>
<li>1 tsp (4g) baking powder</li>
<li>1/4 tsp (2g) salt</li>
<b><i>Cream Cheese Mixture</b></i>
<li>12 oz (350g) cream cheese, room temperature</li>
<li>1/3 cup (65g) sugar</li>
<li>1 egg</li>
<li>zest from one orange</li>
<li>3 tbsp (45ml) orange juice</li>
</ul>
	</section>
	<section>
		<h3>Directions</h3>
		<ol type="1"> 
<li>Preheat the oven to 350 F (180C). Grease and line a 9x13 inch (24x35 cm) baking pan with parchment paper. </li>
<li>Prepare the cream cheese: Mix the cream cheese until smooth. Mix in the sugar, orange zest and orange juice. Incorporate the egg and set aside until ready to use.
<li>In a bowl whisk in the flour, cocoa powder, baking powder and salt. </li>
<li>Melt the chocolate and butter in a medium bowl, over bain-marie, stirring occasionally, until smooth. Remove from heat and whisk in sugar. </li>
<li>Add eggs one at a time and mix well until incorporated. Add espresso coffee, almond extract and vanilla extract. </li>
<li>Gradually incorporate flour mixture until well combined. </li>
<li>Pour chocolate batter into the prepared pan and spread evenly.  Reserve about ¾ cup to use later to create swirls. </li>
<li>Spread the cream cheese mixture over the top. </li>
<li>Spoon the remained ¾ cup chocolate batter over the cream cheese and using a knife create swirls. </li>
<li>Bake for 25-30 minutes until set. Let it cool completely on a cooling rack,cover and refrigerate for several hours or overnight.</li> 
<li>Cut in squares using a hot knife.</li>

	</ol>
