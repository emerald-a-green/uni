	<summary>Opinion</summary>
<p>I can't believe we are already in December. Christmas spirit is already filling the air and all I can think of is cookies, cookies and more cookies. Just hope that I can make them all in time until Christmas day.</p>
<p>These chocolate crinkle cookies are on my "to do" list for a long time. I've seen various recipes but I stopped searching when I found this one..I just knew they are going to be awesome.. They are made using no flour, only egg whites and lots of chocolate. They are gluten free as well for whoever is concerned about it.</p>
<p>They turned exactly the way I imagined crinkle cookies should be, very soft and fudgy inside, not too sweet and crispy on the outside.. The chocolate chunks are melted when you just take a bite .. Really hard to describe.. those who love chocolate as much as I do know what I am talking about.</p>
<p>As they have no flour they are not hard at all, simply melts in your mouth. Incredible rich and chocolaty, perfect for holidays. They can make a beautiful gift idea as well, so don't forget about these cookies when Christmas knocks on your door.</p>
