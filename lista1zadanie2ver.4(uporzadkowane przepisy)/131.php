<h3>Ingredients:</h3>
Makes about 20 cookies
<ul style="list-style-type:circle">
<li>7 oz (200 g) bittersweet chocolate (65-70% cocoa percentage), divided</li>
<li>3 egg whites</li>
<li>2 cups powdered sugar, divided</li>
<li>1/2 cup unsweetened cocoa powder</li>
<li>1 tbsp cornstarch</li>
<li>1/4 tsp salt</li>
<li>almond extract, to taste</li>
<li>extra flavors (orange or lemon zest, peppermint extract, instant espresso, ...), optional</li>
</ul>
	</section>
	<section>
		<h3>Directions</h3>
		<ol type="1"> 
<li>Preheat oven to 350F (180C). Line two baking sheets with parchment paper. Cut half of the chocolate in smaller chunks. Set aside until ready to use.</li>
<li>Melt the other half of the chocolate over bain-marie and set aside to cool slightly.
<li>Meanwhile, whisk together 1 cup of powdered sugar, cocoa powder, cornstarch and salt into a medium bowl.</li>
<li>Using a mixer start whipping the egg whites until they begin to rise and hold their shape.  While whipping, gradually beat in 1/2 cup powdered sugar until stiff and firm, about 2 minutes.</li>
<li>Using low speed of your mixer, gradually add the dry ingredients mixture over the whipped whites. Add melted chocolate, chocolate chunks and almond extract. Stir to combine.</li>
<li>Place 1/2 cup powdered sugar into a bowl. Using your ice cream scoop take equal portions of dough and roll into the sugar, coating thickly. Place on the prepared baking sheet. Repeat with remaining dough, keeping 2 inches space between cookies.</li>
<li>Bake for 10 minutes until cracked. Cool on sheets on rack for 10 minutes. Transfer to rack and cool.</li>

	</ol>
