	<summary>Opinion</summary>
<p>This No bake strawberry chocolate tart is absolutely AMAZING. It is the easiest recipe you can get for such a decadent chocolaty dessert. It takes no more than 15 minutes to put together. Is that easy.</p>
<p>The taste is beyond any description. The crust is made with oreos, whole oreos this time :). It is very crispy and complements so well the soft chocolate ganache on top. Strawberries have always made a good pair with chocolate and when you cut the tart into slices they are so nicely sliced as well. </p>
