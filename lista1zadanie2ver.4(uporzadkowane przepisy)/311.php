<h3>Ingredients:</h3>
Makes about 10-12 servings
<ul style="list-style-type:circle">
<b><i>Crust</b></i>
<li>32 (330g) Oreo cookies</li>
<li>1/2 cup (110g) butter, melted</li>
<b><i>Chocolate Filling</b></i>
<li>7 oz (200g) milk chocolate</li>
<li>3.5 oz (100g) bittersweet chocolate</li>
<li>7 oz (200 ml) heavy cream</li>
<b><i>Topping</b></i>
<li>10 oz (300g) fresh strawberries</li>
<li>chopped nuts (almonds, pistachios, etc), optional</li>
</ul>
	</section>
	<section>
		<h3>Directions</h3>
		<ol type="1"> 
<li>Place oreo cookies into the bowl of a food processor and blend until crumbs form. Add melted butter and pour over the crushed cookies. Process until evenly moistened. Press cookie mixture with the back of the spoon and your fingers where necessary,  into the bottom and edges of a rectangular 5x14 inch (12 x 36 cm) tart pan. Freeze until you prepare the filling, just for few minutes. </li>
<li>In a small saucepan, place cream over medium-low heat for a few minutes to get hot. No boiling or simmering needed. Remove from heat, pour over chopped chocolate and let rest for 1-2 minutes. Stir until dissolved. </li>
<li>Pour chocolate filling over oreo crust, and top with fresh strawberries. </li>
<li>Refrigerate for at least 2 hours or overnight before serving.</li>
	</ol>
