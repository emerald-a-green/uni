<h3>Ingredients:</h3>
Makes about 9-12 servings
<ul style="list-style-type:circle">
<li>9-12 lasagna noodles
<b><i>Chicken Sauce</b></i>
<li>2 tbsp olive oil</li>
<li>1 large onion</li>
<li>2 carrots, finely chopped</li>
<li>2 celery sticks, finely chopped</li>
<li>2 cloves garlic, minced or finely chopped</li>
<li>1 pound (500g) ground chicken</li>
<li>28 oz (800g) tomato puree</li>
<li>1/4 cup (50g) tomato paste</li>
<li>3 tsp sugar</li>
<li>1 tsp oregano</li>
<li>4-5 fresh basil leaves, chopped (or 1 tsp dry basil)</li>
<li>3/4 cup (200 ml) white wine</li>
<li>salt and freshly ground black pepper</li>
<b><i>Bechamel sauce</b></i>
<li>1/2 stick (60 g) butter</li>
<li>1/3 cup (40 g) flour</li>
<li>2 1/4 cup (560 ml) milk</li>
<li>1/2 tsp nutmeg</li>
<li>salt and freshly ground black pepper</li>
<b><i>Topping</b></i>
<li>1 cup (100g), Parmesan, freshly grated</li>

</ul>
	</section>
	<section>
		<h3>Directions</h3>
		<ol type="1"> 
<li>Prepare chicken sauce. In a large pan heat oil over medium high heat. Cook the onion,garlic,  carrot and celery over medium heat until softened, stirring constantly. </li>
<li>Add the chicken and cook until it changes its color. Break up any lumps of meat using a fork.</li>
<li>Add tomato paste, tomato puree, wine, sugar, oregano, basil, salt and pepper to taste. Cover, bring to a boil, reduce the heat and simmer for about 1 1/2 hrs. Set aside. You can also make the sauce a day before, and refrigerate after it cools. Before assembling the lasagna, warm the sauce as it needs to be a bit warm.</li>
<li>Prepare the Bechamel sauce. In a medium sauce pan, melt the butter over medium-low heat. Add the flour and stir until smooth, for about 1 minute, until bubbling. Remove from heat and gradually stir in milk, until mixture is smooth. </li>
<li>Return to heat and stir constantly until the sauce boils and begin to thicken.  Add nutmeg, salt and pepper and set aside until ready to use. Give it a stir from time to time so it doesn't make a skin on the surface.</li>
<li>Preheat oven to 350 F (180C). Grease a 8 X 12 inch (20x30cm)lasagna dish with olive oil or melted butter ( 9x13 inch dish works as well). I used Barilla noodles who didn't required precooking. If your noodles require precooking follow the instructions from the manufacturer and drain well before use. </li>
<li>Assemble the lasagna. Spread a layer of chicken sauce (about 1/3 of the sauce) over the bottom of the baking dish. Spread a thin layer of Bechamel (about 1/2 cup of Bechamel sauce). Place a single layer of noodles over top and press gently. Depending on noodles size might be used 3 to 4 noodles per layer. Repeat spreading half of the remained chicken sauce over the noodles. Spread 1/2 cup Bechamel sauce on top. Add a new layer of noodles, pressing gently. Spread the remained chicken sauce, another 1/2 cup Bechamel and add the last layer of noodles.</li>
<li>Top the last layer of noodles with the remaining Bechamel sauce and sprinkle the freshly grated Parmesan over the top. Cover with aluminum foil making sure it doesn't touch the surface.</li>
<li>Cook for about 25 minutes covered with foil, and another 25 minutes without. If necessary, broil for few minutes until the cheese has the desired golden brown look. </li>
<li>Set aside for about 15 minutes before serving. Serve alongside some fresh salad, or even some toasted bread. It is delicious.
</li>
	</ol>
