	<summary>Opinion</summary>
<p>I've been wanting to post a lasagna recipe for quite some time. Couldn't decide which one should I choose first, the classic with beef, a vegetarian or this one with chicken. I finally decided on this chicken lasagna as it is lighter, and kids seem to love it. Beef lovers, can always replace chicken with beef in this recipe and get a classic beef lasagna.  For a vegetarian lasagna you can replace chicken with mushrooms and you get a delicious hearty meal as well.</p>
<p>I was really excited about how this chicken lasagna turned out. I was afraid it won't look as good as a classic lasagna, or that the chicken would be too dry, but actually it turned just perfect. The lasagna had the perfect texture and taste. It was not too dry, not too soggy either, I could lift it from the dish perfectly from the first slice.</p>
<p>This chicken lasagna tastes incredible good. The sauce is really delicious and very flavorful, I really love using wine in this sauce, it gives a wonderful flavor and I was pretty generous with it :) . The Parmesan on top of Lasagna is my favorite part. It gets such a wonderful taste and color while baking and complements so good this lasagna. I didn't use cheese between layers or additional Mozzarella cheese like others do as I didn't want to overwhelm the recipe with too much cheese. The Parmesan on top was enough to bring it's unique flavor.</p>
<p>To make things easier, you can make the chicken sauce a day before, refrigerate and before assembling the lasagna simply warm it a bit. All you have to do the next day is to make the Bechamel Sauce and lasagna is ready to be assembled. Hope you will give this recipe a try, I am sure you will love it too:)</p>
