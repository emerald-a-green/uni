	<summary>Opinion</summary>
<p>I have discovered this crazy chocolate cake only recently and I must say I fall in love with it immediately. It is somehow very similar to the recipe used in the Chocolate Rose Cake but it is an eggless version. I was required an eggless recipe for quite a long time so here it is. One of the most delicious cakes, moist and chocolaty, full of flavor and almost melting in your mouth.</p>
<p>It is so quick to prepare, no eggs, butter or milk are used and you don’t even need to grease the pan. All ingredients are mixed directly in the pan simply using a whisk.  That easy, but nobody will know after the way it looks and taste.</p>
<p>I’ve made a ganache for the top just to make it more fancy and attractive, but feel free to use any other frosting or topping idea. You can also serve it warm with some ice cream on top.</p>
<p>Definitely a last minute cake you can prepare when guests have just arrived. Everybody will love it and ask for the recipe. Enjoy!</p>
