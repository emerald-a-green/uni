<h3>Ingredients:</h3>
Makes about 8-10 servings
<ul style="list-style-type:circle">
<li>1 1/2 cups (190g) all-purpose flour</li>
<li>1/2 cup (100g) sugar</li>
<li>1/4 cup (30g) unsweetened cocoa powder</li>
<li>1 tsp (6g) baking soda</li>
<li>1/2 tsp (2g) salt</li>
<li>5 tbsp (70g) canola oil</li>
<li>1 tbsp (15ml) vinegar</li>
<li>1 tsp (5g) vanilla extract</li>
<li>1 cup (240ml) hot water</li>
<b><i>Ganache</b></i>
<li>7 oz (200g) semisweet chocolate, chopped</li>
<li>1/2 cup (120g) whipping cream</li>
</ul>
	</section>
	<section>
		<h3>Directions</h3>
		<ol type="1"> 
<li>Preheat oven to 350F (180C). </li>
<li>In an 8 inch (20cm) springform pan add flour, sugar, cocoa powder, baking soda and salt. Whisk to combine. </li>
<li>Add the oil, vinegar, vanilla and hot water and whisk again until completely combined. </li>
<li>Use a spatula to spread evenly and bake for 25-30 minutes until a toothpick inserted into the center of the cake comes out clean. </li>
<li>Let it cool slightly and remove the sides of the pan. </li>
<li>Prepare the chocolate ganache. Heat the cream in a small sauce pan, until almost boiling to the edges. Remove from heat and add chopped chocolate. Let sit 1 minute and then stir until smooth. </li>
<li>Spread the ganache on top of the cake leaving 1 inch (2.5cm) border at edge. </li>
<li>Use a spoon to create a flower shape. </li>
<li>Let the ganache set a bit at room temperature before serving.</li>

	</ol>
