<h3>Ingredients:</h3>
Makes about 2-3 servings
<ul style="list-style-type:circle">
<li>3 cups (300 g) baby button mushrooms
<li>1 medium onion, sliced
<li>1 clove garlic, chopped
<li>2 tbsp olive oil
<li>1 tsp soy sauce
<li>1/4 cup (60 ml) white wine
<li>Salt and freshly ground black pepper
<li>fresh parsley</li>
</ul>
	</section>
	<section>
		<h3>Directions</h3>
		<ol type="1">
<li>Clean baby mushrooms with a damp towel, cut the ends if necessary. If you use bigger mushrooms cut them in halves, quarters, or slices.   
<li>Heat oil in a medium skillet over medium-high heat. Add onion and garlic and cook for about 3-5 minutes until softened. 
<li>Add mushrooms and cook with the onion for about 8-10 minutes stirring frequently. 
<li>Add soy sauce, salt, pepper and wine and cook until wine evaporates, maybe another 3 to 5 minutes. 
<li>Remove from heat and sprinkle fresh parsley on top. Serve immediately.</li>
	</ol>
