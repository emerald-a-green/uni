	<summary>Opinion</summary>
<p>I’ve really wanted to share with you before Christmas this wonderful  Red Velvet Cake. It has a soft and moist interior with a great flavor combination coming from vanilla, cocoa powder and buttermilk.</p>
<p>This cake has a wonderful red color crumb and frosted with a white cream cheese frosting which I simply adore. I’ve seen various Red Velvet Cakes made in different ways and I’ve finally decided on the perfect recipe to share with you. I didn’t want on overwhelmingly sweet cake so I reduced the amount of sugar used in cake batter and in frosting as well. This cake is very well balanced and makes an impressive dessert for winter holidays.</p>
