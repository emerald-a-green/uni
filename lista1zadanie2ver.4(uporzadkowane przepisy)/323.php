<h3>Ingredients:</h3>
Makes about 10-12 servings
<ul style="list-style-type:circle">
<b><i>Cake Batter</b></i>
<li>2 2/3 cups (310g) cake flour</li>
<li>2 tbsp (16g) unsweetened cocoa powder</li>
<li>1 tsp (5g) baking soda</li>
<li>1 tsp (5g) salt</li>
<li>1/2 cup (113g) unsalted butter, room temperature</li>
<li>1 1/2 cup (300g) sugar</li>
<li>2 eggs</li>
<li>1 cup (220g) vegetable oil</li>
<li>1 tsp (5ml) white vinegar</li>
<li>2 tsp (10g) vanilla extract</li>
<li>1 cup (240g) buttermilk, room temperature</li>
<li>1- 2 oz (30g) red food coloring gel</li>
<b><i>Cream Cheese Frosting</b></i>
<li>17 oz (500g) cream cheese, room temperature</li>
<li>1 1/3 cup (300g) heavy whipping cream, chilled</li>
<li>1 tsp (5g) vanilla extract</li>
<li>1 1/4 cup (156g) powdered sugar</li>
</ul>
	</section>
	<section>
		<h3>Directions</h3>
		<ol type="1"> 
<li>Preheat oven to 350F (180C). Grease with butter and dust with flour two 9 inch (23 cm) round cake pans. </li>
<li>In   a large bowl sift together the flour, cocoa powder, baking soda and salt and whisk to combine. </li>
<li>In another bowl mix butter with sugar until combined. Incorporate eggs one at a time. Mix in oil, vinegar,vanilla extract and buttermilk. Incorporate flour mixture until well combined. </li>
<li>Gradually add red food coloring until the desired color is reached. </li>
<li>Pour the batter evenly into the prepared pans. </li>
<li>Bake for about 30 minutes until a toothpick inserted into the center comes out clean. </li>
<li>Let pans cool on a cooling rack for 10 minutes. </li>
<li>Remove the cakes from the pan and let them cool completely. </li>
<li>Prepare the frosting. In a bowl sift the powdered sugar. Beat the cream cheese with powdered sugar and vanilla extract until smooth. </li>
<li>In another bowl whip the cream until stiff peaks form. Gradually fold into the cream cheese mixture until well combined. </li>
<li>Assemble the cake. Cut the tops of the cakes to create flat surfaces. Make crumbs out of the left pieces of cake and set aside.</li> 
<li>Place 1 cake layer on your serving plate. Spread evenly with a bit less than half of frosting. Add the second layer of cake. Gradually spread the remaining frosting on top and sides of the cake. </li>
<li>Decorate the cake with remained cake crumbs if desired.</li>
<li>The cake is ready to serve. </li>
<li>The leftover frosted cake can be covered and refrigerated for up to a week. Serve at room temperature.</li>
	</ol>
