<h3>Ingredients:</h3>
Makes about 3-4 servings
<ul style="list-style-type:circle">
<li>2 boneless chicken breast halves (500g)
<li>1/4 cup (56g) cream cheese
<li>1/2 tsp (2g) garlic powder
<li>1 tbsp chives, chopped
<li>Salt and freshly ground black pepper
<li>1/4 cup (56g) oil packed sun-dried tomatoes, chopped
<li>1/2 cup (50g) mozzarella cheese
<li>baby spinach leaves
<li>2 tbsp olive oil</li>

</ul>
	</section>
	<section>
		<h3>Directions</h3>
		<ol type="1">
<li>Preheat oven to 350 F (180C). 
<li>Flatten the chicken breasts using a meat mallet until they are about ¼-½ inch  (0.6-1 cm) thickness.
<li>In a small bowl mix cream cheese with garlic powder, chives, salt and pepper. 
<li>Spread half of the cream cheese mixture on each chicken breast. Top with chopped sun-dried tomatoes, mozzarella cheese and spinach leaves. Add more salt and pepper and  roll up the breasts securing with toothpicks. 
<li>Heat the olive oil in a oven proof skillet and brown the chicken  on all sides. 
<li>Place skillet in oven and bake chicken for an additional 35-40  minutes, until chicken is cooked through. 
<li>Let the chicken rest for about 15 minutes before slicing.
</li>
	</ol>
