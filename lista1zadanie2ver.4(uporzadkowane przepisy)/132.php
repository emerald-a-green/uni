	<summary>Opinion</summary>
<p>Everybody who follow my blog have noticed I love chocolate desserts. These chocolate coconut brownies are simply impossible to resist, fudgy brownies at the base topped with a coconut filling and chocolate ganache on top. For those who love coconut desserts these are one of the best brownies ever.</p>
<p>The recipe is really easy to prepare but require some time for chilling, so it’s not the kind of dessert you can serve right away, but definitely worth the waiting time. I did not add extra sugar on the coconut filling and the sweetness is very well balanced with the semisweet chocolate used. Hope you will enjoy these brownies as much as we did:)</p>
