<h3>Ingredients:</h3>
Makes 16 servings
<ul style="list-style-type:circle">
<li>3 oz (100g) semisweet chocolate</li>
<li>3/4 cup (170g) butter</li>
<li>1 tsp (1g) instant coffee powder</li>
<li>1 cup (200g) sugar</li>
<li>3 eggs</li>
<li>1 tsp (5g) vanilla extract</li>
<li>1 tbsp (8g) dark cocoa powder</li>
<li>1/2 tsp (2g) salt</li>
<li>3/4 cup (95g) flour</li>
<b><i>Coconut Filling</b></i>
<li>12 oz (340g) sweetened condensed milk</li>
<li>2 1/2 cups (200g) unsweetened coconut flakes</li>
<b><i>Chocolate Ganache</b></i>
<li>1/2 cup (100g) whipping cream</li>
<li>4 oz (120g) semisweet chocolate</li>
</ul>
	</section>
	<section>
		<h3>Directions</h3>
		<ol type="1"> 
<li>Preheat oven to 350F (180C). Butter the sides and bottom of a 8x8 inch (20 X 20 cm) or 9x9 inch (23x23cm) baking pan. Line the pan with parchment paper. </li>
<li>Melt the chocolate and butter in a medium bowl, over bain-marie, stirring occasionally, until smooth.  Remove the pan from heat but keep the bowl over the water. Add instant coffee, stir in the sugar, then remove the bowl from the pan. Cool slightly. </li>
<li>Incorporate the eggs one at a time into the mixture and whisk until well combined. Stir in the vanilla extract then add the flour, salt, cocoa powder and stir well. </li>
<li>Pour the batter into the prepared pan and bake for about 30 minutes until a toothpick inserted in the center comes out with a few moist crumbs sticking to it. 
<li>Let the brownies cool completely. </li>
<li>In a bowl combine sweetened condensed milk and coconut flakes. Spread evenly on top of brownies. </li>
<li>Prepare the chocolate ganache. Heat the cream in a small saucepan. When the cream just begins to boil at the edges, remove from heat and pour over the chopped chocolate. Let sit one minute, then stir until smooth. Spread it evenly and refrigerate for at least 1 hour before serving. </li>
<li>Cut into squares and serve.</li>

	</ol>
