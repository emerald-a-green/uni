	<summary>Opinion</summary>
<p>Everybody love no-bake desserts as they are easy and quick to prepare, perfect for those moments whenever you want something sweet but don’t feel like turning on the oven. This Chocolate Biscuit Cake is one of them, and I guess many of us are familiar with it since childhood, in various shapes and forms. Chocolaty, rich dessert made with crushed biscuits, chocolate syrup and toasted nuts, hardly not to fall in love with it.  This is the kind of recipe that is never refused, looks quite impressive taking in count the little time involved, tastes awesome and everybody love it every time I make it. </p>
<p>The recipe is really easy to prepare, there is no way you can wrong with it. I’ve used my mother’s recipe she had for years and just added chocolate ganache on top. Even kids can be involved in the process as they love crushing biscuits and pressing the mixture into the pan. You can always make variations to this chocolate biscuit recipe, add different flavors like orange zest, rum, almond extract, dried fruits. I usually like to keep it simple and enjoy the rich chocolate flavor.</p>
<p>I am pretty excited by my new no-bake Delicake cake ware, and I highly recommend it. It  behaves absolutely great, it’s non stick, the sleeve removes easily and love the fact it can be either refrigerated or frozen. This chocolate biscuit cake recipe fit perfectly for my new cake ware.</p>
