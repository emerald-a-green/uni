<h3>Ingredients:</h3>
Makes about 10 servings
<ul style="list-style-type:circle">
<li>28 oz (800g) digestive biscuits, tea biscuits</li>
<li>1 cup (100g) nuts (walnuts, hazelnuts), toasted, slightly chopped</li>
<b><i>Chocolate Syrup</b></i>
<li>1 cup (200g) sugar</li>
<li>1/2 cup (60g) unsweetened cocoa powder</li>
<li>1 cup (240 ml) water</li>
<li>2/3 cup (150g) butter</li>
<li>1 tsp vanilla extract</li>
<b><i>Chocolate Ganache</b></i>
<li>1/2 cup (120 g) whipping cream (35% fat)</li>
<li>4 oz (120g) bittersweet chocolate, chopped</li>
</ul>
	</section>
	<section>
		<h3>Directions</h3>
		<ol type="1"> 
<li>Break the biscuits (cookies) into small pieces into a large bowl. 
Heat a non-stick pan over medium high heat and toast walnuts or hazelnuts for about 5 minutes, stirring frequently to toast evenly.  Add toasted nuts over the biscuits. </li>
<li>Prepare the chocolate syrup. In a medium sauce pan add sugar and cocoa powder. Stir to combine and gradually add water, while stirring to combine well. Add the butter and place over medium heat. Bring to boil for about 7-8 minutes stirring constantly. Remove from heat and add vanilla extract. Set aside to cool slightly for about 10-15 minutes. </li>
<li>Pour the syrup over the biscuits and nuts and combine well using a large spatula or spoon. </li>
<li>Transfer the mixture into a 9 inch (23 cm) non-stick round cake ware (like the one I’ve used from Delicake) or use a 9 inch (23 cm) springform pan, bottom lined with parchment paper. </li>
<li>Press well using the back of the spoon or an offset spatula. </li>
Cover and refrigerate for about 30 minutes to 1 hr before preparing the chocolate ganache. </li>
<li>Prepare Chocolate Ganache. Place cream into a small saucepan and heat until just begins to boil. </li>
<li>Pour hot cream over the chopped chocolate and let sit for 1 minute. Stir until smooth. </li>
<li>Pour ganache over the chocolate biscuit cake and refrigerate to set for about 3-4 hours or overnight before serving. </li>
<li>Decorate with toasted nuts if desired, cut into slices and serve.</li>
	</ol>
