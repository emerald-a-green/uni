<h3>Ingredients:</h3>
Makes 18 cheese bombs
<ul style="list-style-type:circle">
<li>2 cups (250g) flour
<li>1 tbsp (15g) sugar
<li>1 1/2 tsp garlic powder
<li>1/4 tsp salt
<li>2 1/2 tsp (10g) baking powder
<li>5 tbsp (70g) cold unsalted butter, cut into small cubes
<li>3/4 cup (200g) cold buttermilk</li>
<b><i>For Filling</b></i>
<li>5 oz (140g) Mozzarella</li>
<b><i>For brushing the tops</b></i>
<li>2 tbsp (30g) butter
<li>1/4 tsp salt
<li>1 tsp fresh parsley, chopped
<li>1 clove garlic, minced</li>

</ul>
	</section>
	<section>
		<h3>Directions</h3>
		<ol type="1">
<li>Preheat oven to 400 F (200 C). Line a baking sheet with parchment paper. 
<li>In a medium-large bowl whisk together flour, sugar, salt, baking powder and garlic powder. Add the butter cubes, and using a pastry cutter or fork cut the butter until the pieces are no bigger than peas.
<li>Incorporate the cold buttermilk and knead gently to bring it together. Refrigerate the dough for 10 minutes.
<li>Meanwhile cut the Mozzarella in 18 pieces. 
<li>Dust the working surface with flour and roll the dough into a 12x15 inch (30x40cm) rectangle. Cut in 18 pieces.
<li>Place a piece of cheese in the middle of each piece of dough and wrap the dough around the cheese making sure the edges are well sealed. Roll in your hands to form a ball and place in the prepared baking sheet.
<li>Bake for about 15-18 minutes until golden brown.
<li>While they bake, prepare the butter mixture. Melt the butter into a small sauce pan, add garlic, parsley and salt and cook for 1 minute. Remove from heat. Brush the tops of the cheese bombs with butter mixture and serve hot.
</li>
	</ol>
