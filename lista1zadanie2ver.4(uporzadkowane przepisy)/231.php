	<summary>Opinion</summary>
<p>I’ve seen this kind of cheese bombs on Pinterest few time ago and searched for a recipe. All the recipes I have seen were using premade biscuit dough. Where I live I can’t find this dough in grocery stores so I decided to try making them from scratch. I already had a delicious recipe for cheddar biscuits so I’ve used it to create these amazing Garlic Cheese Bombs and really worked great. This time I’ve used Mozzarella but cheddar cheese should work as well.</p>
<p>These cheese bombs are easy to prepare even if you make everything from scratch.. should not take you more than 20 minutes to have this prepared and placed in the oven. They are soft and flaky.. and that gooey cheese inside worth all the effort. As holidays are knocking to our doors this recipe might be a great appetizer for this special time of the year.</p>
