	<summary>Opinion</summary>
<p>I am glad to share with you another great homemade Twix recipe. This one is easy to prepare as well as the other Chocolate Caramel Bars recipe I’ve shared few time ago,  but it is an eggless version made with sweetened condensed milk.</p>
<p>It is the first time trying to make a caramel without caramelizing the sugar first and I was surprised by the great result I obtained this way. The caramel turned chewy and gooey but still soft, exactly as needed for this recipe.</p>
<p>You can hardly beat these candy bars,  they are rich, buttery and chocolaty, a thick buttery crust topped with chewy caramel and a thick chocolate layer on top. Simply amazing and quite addictive..</p>
