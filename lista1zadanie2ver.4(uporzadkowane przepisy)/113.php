<h3>Ingredients:</h3>
Makes about 18 bars
<ul style="list-style-type:circle">
<b><i>Crust</b></i>
<li>1/2 cup (113g) butter, room temperature</li>
<li>2 tbsp (30g) sugar</li>
<li>1 1/4 cup (160g) all-purpose flour</li>
<li>Pinch of salt</li>
<b><i>Caramel</b></i>
<li>1 can (14oz -397g) sweetened condensed milk</li>
<li>3 tbsp (45g) butter</li>
<li>1/4 cup (50g) brown sugar</li>
<li>1/2 tsp (3g) salt</li>
<li>1 tsp (5g) vanilla extract</li>
<b><i>Chocolate Topping</b></i>
<li>7 oz (200g) dark or milk chocolate</li>
<li>2 tbsp (28g) butter</li>
</ul>
	</section>
	<section>
		<h3>Directions</h3>
		<ol type="1"> 
<li>Preheat oven to 350F (180C). </li>
<li>Slightly grease a 8x8 inch (20x20cm) square pan and line with parchment paper, in such a way that some edges overhang to make it easy to lift it up once it's set. </li>
<li>Prepare the crust. In a large bowl cream together the butter, sugar and salt until light and fluffy. </li>
<li>Sift the flour and incorporate into the butter mixture until crumbly and mixture holds together when squeezed. </li>
<li>Press into the bottom of the pan using the back of  a spoon and bake for 20 minutes. Remove from the oven and allow to cool slightly. </li>
<li>Make the caramel.  Place all the ingredients into a saucepan and place over low-medium heat, stirring continuously.  Bring to a boil and continue cooking for 5 minutes more stirring continuously. . Pour over the crust and bake for 12 -15 minutes at 350F (180C).Let it cool slightly. </li>
<li>Melt the chocolate with butter over bain-marie. Spread melted chocolate on top of the caramel. </li>
<li>Refrigerate for about 30 minutes or until chocolate is set.</li>

	</ol>
