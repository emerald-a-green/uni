<h3>Ingredients:</h3>
Makes 12 cupcakes
<ul style="list-style-type:circle">
<li>3/4 cup (95g) all-purpose flour</li>
<li>1/3 cup (40g) cocoa powder</li>
<li>1/2 tsp (2g) salt</li>
<li>1/2 tsp (2g) baking powder</li>
<li>1/4 tsp instant coffee</li>
<li>1/3 cup (75g) unsalted butter, melted</li>
<li>2/3 cup (135g) sugar</li>
<li>3 eggs</li>
<li>1 tsp (5g) vanilla extract</li>
<li>2/3 cup (150g) sour cream</li>
<li>3 oz (80g) semisweet chocolate, melted</li>
<b><i>Irish Cream Frosting</b></i>
<li>9 oz (250g) cream cheese, room temperature</li>
<li>1/2 cup (60g) powdered sugar</li>
<li>3 tbsp (40ml) Baileys Irish Cream</li>
<li>2/3 cup (200g) whipping cream, 35% fat</li>
</ul>
	</section>
	<section>
		<h3>Directions</h3>
		<ol type="1"> 
<li>Preheat the oven to 350°F (180C). Line a muffin pan with paper liners. 
In a medium bowl combine the flour, cocoa powder, instant coffee, baking powder and salt. </li>
<li>In another bowl mix the butter and sugar until thick and creamy. Add eggs, one at a time, vanilla extract and mix to combine. </li>
<li>With the mixer on low, alternate adding gradually flour mixture and sour cream until all is well incorporated. </li>
<li>Add the melted chocolate and mix to incorporate. </li>
<li>Scoop batter evenly among liners, about 2/3 or 3/4 full each. </li>
<li>Bake for 18- 20 minutes or until a toothpick inserted into the center comes out with a few crumbs. </li>
<li>Let them cool on the pan for about 5 minutes, then remove to a cooling rack to cool completely before frosting. </li>
<li>Prepare the cream cheese frosting:  In a bowl, beat cream cheese and then incorporate powdered sugar. Add Baileys Irish cream and mix to combine. In another bowl whip the cream with an electric mixer until it forms stiff peaks. Gradually fold together. </li>
<li>Place the frosting into a piping bag fitted with a (11mm) open star tip and pipe cupcakes. </li>
<li>Decorate with cocoa powder if desired. </li>
<li>Serve immediately or refrigerate until ready to serve. If refrigerated let them get to almost room temperature before serving.</li>

	</ol>
