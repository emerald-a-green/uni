	<summary>Opinion</summary>
<p>I recently have discovered the perfect recipe for chocolate cupcakes and really hope you will give these a try. They are rich, chocolaty and with a divine moist interior. And that is not all - the cream cheese frosting with a hint of Irish cream tastes absolutely amazing and goes so well with the chocolate cupcakes. </p>
<p>Not only they are perfect for celebrating St Patrick’s Day but are simply wonderful for any occasion. </p>
<p>If you don't want to use Irish cream for the frosting feel free to use vanilla or any other flavoring that you prefer, but the cream cheese frosting is a must, as I consider it so much better than any buttercream that is usually used to decorate cupcakes.</p>
