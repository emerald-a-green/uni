	<summary>Opinion</summary>
<p>I have been asked for so many times how to make Nutella Cheesecake without baking, and not involve eggs. So I finally came up with this delicious no-bake Nutella Swirl Cheesecake recipe which is very easy and quick to prepare, and looks quite impressive.</p>
<p>The oreo crust is pressed on the bottom and up the sides which creates a wonderful edge and a great presentation, not to mention it has a wonderful texture as well. The filling is divided in two, one of cream cheese and one with Nutella,  swirled from top to bottom. </p>
<p>The cheesecake is decadent, rich and smooth with a crispy Oreo crust, full of nutella flavor and completely addictive. A perfect treat for special occasions or for whenever you are craving a quick nutella dessert.</p>
