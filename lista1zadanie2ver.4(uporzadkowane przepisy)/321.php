<h3>Ingredients:</h3>
Makes about 8-10 servings
<ul style="list-style-type:circle">
<b><i>Crust</b></i>
<li>12 oz (340g) oreo cookies</li>
<li>1 stick (110g) unsalted butter, melted</li>
<b><i>Filling</b></i>
<li>26 oz (750g) cream cheese, room temperature</li>
<li>7 oz (200g) whipping cream (35% fat), cold</li>
<li>1/2 cup (140g) Nutella</li>
<li>2 tsp (10g) vanilla extract</li>
<li>1/2 cup (65g) powdered sugar</li>
</ul>
	</section>
	<section>
		<h3>Directions</h3>
		<ol type="1"> 
<li>Place the oreo cookies into the bowl of a food processor and crush until crumbs form. Add melted butter and process until evenly moistened. </li>
<li>Press the mixture into the bottom and up the sides of a greased 8 inch (20 cm) springform pan using the back of a spoon and your fingers where necessary.  Refrigerate until the filling is prepared. </li>
<li>Prepare the filling. In a mixing bowl place the cream cheese, powdered sugar and vanilla extract and mix until smooth.</li>
<li>In another bowl mix the cream until soft peaks form. Gradually fold the whipped cream into cream cheese mixture. </li>
<li>Divide the mixture in two. Fold Nutella in one half of the mixture. </li>
<li>Place the mixtures into the prepared pan on two layers. Start with 3 large spoons of cream cheese mixture with space between them and them fill the gaps with 3 large spoons of Nutella mixture. Repeat for the second layer. Slam the sprinform pan on the counter top to spread the mixture.  Use a knife to create swirls on top and bottom as well. </li>
<li>Refrigerate for 6 hours or even overnight to set.</li>

	</ol>
