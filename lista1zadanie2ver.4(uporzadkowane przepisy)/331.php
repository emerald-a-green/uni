<h3>Ingredients:</h3>
Makes about 8-10 servings
<ul style="list-style-type:circle">
<b><i>Cake</b></i>
<li>3/4 cup (95g) all-purpose flour</li>
<li>1 tsp (4g) baking powder</li>
<li>1/2 tsp (2g) salt</li>
<li>4 eggs</li>
<li>3/4 cup (150g) sugar</li>
<li>1 tsp (5g) vanilla extract</li>
<li>2 tbsp (28g) canola oil</li>
<b><i>Strawberry Jam Filling</b></i>
<li>10 oz (300g) strawberries, cut into smaller pieces</li>
<li>1/4 cup (50g) sugar</li>
<li>1 tbsp (15ml) water</li>
<b><i>Cream Cheese Filling</b></i>
<li>9 oz (250g) cream cheese, room temperature</li>
<li>1 cup (240g) whipping cream, chilled</li>
<li>1/2 cup (60g) powdered sugar</li>
<li>1 tsp (5g) vanilla extract</li>
<b><i>For dusting</b></i>
<li>Powdered sugar</li>


</ul>
	</section>
	<section>
		<h3>Directions</h3>
		<ol type="1"> 
<li>Prepare the strawberry jam filling. In a small saucepan put the strawberries, sugar and water. Bring to a simmer, stirring occasionally. Press the strawberries with the back of the spoon to speed up the process. Simmer until the mixture is thick, for about 15-20 minutes.  Remove from heat and let cool completely before using. </li>
<li>Prepare the cake. Preheat the oven to 350F (180C). Butter a 12X16 inch (30x40 cm) baking tray and line it with parchment paper. </li>
<li>In a medium bowl whisk flour with baking powder and salt. Set aside. </li>
<li>In a large bowl add eggs, sugar and vanilla extract. Beat with an electric mixer until lemon yellow and foamy. Add oil and mix until well incorporated. Gradually add flour mixture. </li>
<li>Pour the batter into the prepared baking tray and spread evenly into the corners using the back of the spatula. </li>
<li>Bake for 12-15 minutes until a toothpick inserted into the center comes out clean. </li>
<li>Take a kitchen towel and generously sprinkle with powdered sugar. This helps the cake not stick to the towel. Flip the cake over the towel. Remove the parchment paper and dust with powdered sugar. </li>
<li>While still hot roll the cake up in the towel. You will have to do it while is still hot/warm otherwise it cracks. Let cool completely. </li>
<li>Prepare the cream cheese filling. In a bowl, beat cream cheese with powdered sugar and vanilla. In another bowl whip the cream with an electric mixer until it forms stiff peaks. Fold together. </li>
<li>Unroll the cake and spread the strawberry filling evenly over the cake. 
Spread the cream cheese filling on top of the strawberry filling. </li>
<li>Roll the cake back up. Refrigerate for at least 1 hour before serving. </li>
<li>Sprinkle with more powdered sugar before serving and decorate with fresh strawberries.</li>

	</ol>
