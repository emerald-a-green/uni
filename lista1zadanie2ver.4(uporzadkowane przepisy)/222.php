<h3>Ingredients:</h3>
Makes about 4 servings
<ul style="list-style-type:circle">
<li>1 1/2 pound (680g) 4 boneless, skinless, chicken breasts</li>
<li>1/2 cup (120g) Stubbss Chicken Marinade</li>
<li>1/3 cup (80g) Stubbs Spicy BBQ Sauce</li>
<li>8 bacon slices</li>
</ul>
	</section>
	<section>
		<h3>Directions</h3>
		<ol type="1"> 
<li>Cut each chicken breast in half lengthwise. </li>
<li>Season chicken  with Stubbs Chicken Marinade, which is a blend of citrus, onion and garlic that packs a fresh flavor with undertones of mustard seed and ginger while the acid from the citrus will make a moist and tender meat. Refrigerate for at least 1 hour. </li>
<li>Heat oven to 350 degrees. </li>
<li>Brush all sides of chicken with Stubbs Spicy BBQ Sauce ((tangy tomato, vinegar, molasses, habanero, cayenne and black peppers) </li>
<li>Wrap a piece of bacon around each piece of chicken. </li>
<li>Place chicken on a greased grill. Bake for 15 minutes. Baste with BBQ Sauce and return to oven for 10 minutes. </li>
<li>Baste one more time and return to oven for 5 minutes. </li>
<li>Serve with some fresh salad and more sauce.</li>
	</ol>
