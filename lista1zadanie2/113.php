<h3>Ingredients:</h3>
Makes about 20 cheesecake bites
<ul style="list-style-type:circle">
<li>10 oz (300g) cream cheese, softened to room temperature
<li>1/3 cup (40g) powdered sugar
<li>1 tsp (5g) vanilla extract
<li>1/2 cup (80g) fresh raspberries
<li>5 oz (150g) digestive biscuits/graham crackers
<li>1 cup (240g) whipping cream
<li>fresh raspberries for serving</li>
</ul>
	</section>
	<section>
		<h3>Directions</h3>
		<ol type="1">
<li>Place cream cheese in a bowl and mix with sugar and vanilla extract. Add fresh raspberries and mix until incorporated. 
<li>In another bowl whip the cream until stiff peaks form. Gently fold the whipped cream into the cream cheese mixture. 
<li>Cover with plastic wrap and freeze for 2-3 hours until able to form into balls. 
<li>Crash digestive biscuits or graham crackers using a food processor or place them in a ziplog bag until crumbs form. 
<li>Using an ice cream scoop take equal portions of cream cheese mixture and roll into crumbs. 
<li>Place the balls into a parchment paper lined baking pan and freeze for a couple of hours. 
<li>Let the cheesecake bites sit for 5 minutes at room temperature before serving.Serve alongside fresh raspberries.
	</ol>
