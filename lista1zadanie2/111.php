	<section>
<h3>Ingredients:</h3>
Makes about 16 servings
<ul style="list-style-type:circle">
<li>2 1/2 cups (400g) pitted dates</li>
<li>1 1/2 cups (150g) walnuts</li>
<li>1/2 cup (60g) cocoa powder</li>
<li>2 tbsp (20g) cocoa nibs, optional</li>
<li>1 1/2 tsp (7g) vanilla extract</li>
<li>1 tbsp (15g) coconut oil, melted</li>
<li>1/4 tsp (2g) salt</li>
</ul>
<h4>Frosting</h4>
<ul style="list-style-type:circle">
<li>1/4 cup (30g) cocoa powder</li>
<li>1/4 cup (80g) honey or agave/maple syrup</li>
<li>2 tbsp (30g) coconut oil, melted</li>
<li>1/2 tsp (2.5g) vanilla extract</li>
</ul>
	</section>
	<section>
		<h3>Directions</h3>
		<ol type="1">
<li>Place the walnuts, dates, walnuts, cocoa powder, vanilla extract, coconut oil and salt into the bowl of a food processor.  Process until completely combined and the mixture holds together. </li>
<li>Slightly grease a 8x8 inch (20x20cm) square pan and line with parchment paper, in such a way that some edges are longer to make it easy to lift it up once it's set. </li>
<li>Spread the mixture into the prepared baking pan and press with the back of a wet spoon to create a smooth surface. </li>
<li>Prepare the frosting. In a small bowl combine the cocoa with honey (agave/maple syrup), coconut oil and vanilla extract.</li> 
<li>Spread evenly over the mixture in the pan. </li>
<li>Refrigerate for 2 hours to set. Keep leftovers refrigerated.</li>
	</ol>
