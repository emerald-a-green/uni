	<summary>Opinion</summary>
	<p>Summer calls for no bake recipes as much as possible and as I was craving some chocolate dessert lately,  I found on Pinterest some recipes of no bake brownies. The one that really caught my attention was this easy recipe from Chocolate Covered Katie that looks really awesome.<br></p>
 
<p>I decided to give it a try, didn’t change the recipe too much, just added a bit more cocoa powder, some coconut oil instead of water, some cocoa nibs for extra texture and I must say the recipe is far better than expected.<br></p>
 
<p>These gluten-free brownies are not only healthy but turned fudgy, gooey, chocolaty, nutty, hardly not to fall in love with them from the first bite. Depending on the ingredients you use you can make them raw-vegan also.<br></p>
 
<p>As one of the main ingredients is cocoa powder is really important to use a good quality cocoa with a great chocolate flavor to enjoy this recipe to its best.</p>
