  Some of those extinct organisms might be microfossils entombed in rock. Or, much like the way an abandoned ant colony would leave behind anthills, long-dead microbes may have crafted rocky structures that are preserved in fossil form.<br>
  To narrow down the search, sensitive scientific instruments will sniff out biosignatures—minerals and molecules secreted by ancient life.<br>
  NASA already has a fairly good idea of the most promising places to look, including dried-up lake beds and extinct hydrothermal vents. But competition for the ultimate destination will be stiff, given the rover’s limited range.</div>
  <div class="flex-item-yellow">“On Earth, you could have 50 graduate students walking all over western Australia looking for just the right place for ancient fossils,” says project scientist and Caltech geochemist Ken Farley.<br>
  “We're not going to get that luxury. We're going to land in one place and cruise around an area that might be over a 15-kilometer [9.3-mile] linear distance.”<br>
  While selecting the right spot for the Mars 2020 rover will be daunting, there is cause for optimism: Unlike seismically active Earth, Mars as a whole is an impeccably preserved fossil.
  </div>
  <div class="flex-item-green">“On Earth, our ancient rock record has been through the washing machine and the ringer so many times that the fact that anything still retains any signature of its age is a miracle,” says Brown University’s Jack Mustard, one of the experts consulting with NASA on its fossil-finding mission<br>
  “The rocks on Mars would not have been processed to the same extent, would not have been beat up as much, would not have been stretched and squished and heated and buried and exhumed,” he says.</div> 
  <div class="flex-item-orange">Still, NASA’s first and only encounter with a suspected Martian fossil underscores the challenges of finding evidence of extinct microbial life on another world.<br>
  Twenty years ago, a research team led by David McKay—a prominent NASA scientist who had trained the Apollo astronauts in geology—announced that they had found microscopic fossils. The primitive, bacteria-like organisms showed up inside a four-billion-year-old Martian meteorite that fell to Earth some 13,000 years ago.</div>
