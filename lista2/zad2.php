<p>A rover headed for the red planet will perform an unprecedented search for rocky remnants of dead Martians—so where should we send it?</p>
</div>
<br>
<div id="paragraf2">
<p>Nearly four billion years ago, when Earth was coming alive, Mars was gradually choking to death. The thick atmosphere that had warmed the red planet was leaking into space, and plummeting temperatures caused Martian lakes and rivers to freeze, turning the wet surface into a dry wasteland.</p>
</div>
<div id="paragraf3">
<p>But it’s possible life took root in those early years. And very soon, a NASA robot will arrive at Mars with the goal of collecting rock samples that might contain ancient fossils, perhaps helping to answer one of humanity’s most fundamental questions: Are we alone in the universe?</p>
</div>
<br>
<div id="paragraf4">
<p>First though, would-be Martian fossil hunters will have to decide where, exactly, to send that robot. (Also see "Inside the High-Risk, High-Stakes Race to the Red Planet.")</p>
</div>
<div id="paragraf5">
<p>Currently known as Mars 2020, the next-generation rover will carry a sophisticated mobile geology lab designed to search for signs of tiny dead Martians—single-celled algae and bacteria that are the planet’s most likely ancient inhabitants.</p>
</div>
