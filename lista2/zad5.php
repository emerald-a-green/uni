<h3>CLAY, CHERT, AND CAULIFLOWER</h3>
Geologists will face the same burden of proof when they get their hands on rock samples collected using the Mars 2020 rover. But unlike ALH84001—a random chunk of Mars hurled into space—these rocks will be selectively chosen based on qualities that make them strong candidates for preserving identifiable remnants of Martian life.
<h3>FOR INSTANCE</h3> the Mars 2020 fossil hunters will look for chert and silica—two types of minerals that, on Earth, are ideal for entombing and preserving biological material. Clay mineral deposits are also happy hunting grounds for geologists, since their presence implies there was once a lot of standing water that existed over a long period of time.
<h3>STROMATOLITES</h3>are another type of formation that are prime real estate for extant life. On Earth, these layered rocks are created in shallow water by microbial mats—wafer-thin colonies of tiny organisms held together by a mucus-like substance. The mats act like flypaper, trapping sediment. Layers gradually accumulate as the organisms repeatedly push their way upward through the silt.
<h3>WHERE THE WILD THINGS WERE</h3>
When seeking signs of life, NASA’s mantra is “follow the water.” The space agency has even designated “special regions” on the red planet that could possibly harbor modern microbial life, such as liquid saltwater that periodically flows downhill on certain steep crater walls.<br>But exploring these regions presents challenges for planetary protection—spacecraft are never entirely microbe-free, and there are risks that visiting special regions on Mars will contaminate them and hamper our hunt for alien life.
